def circular_shift(value, shift_amount):
    shift_amount %= 64
    shifted_left = (value << shift_amount) & 0xFFFFFFFFFFFFFFFF
    shifted_right = (value >> (64 - shift_amount)) & 0xFFFFFFFFFFFFFFFF
    return shifted_left | shifted_right


def apply_s_boxes(bits, SBOX):
    result = []
    for i in range(8):
        block = (bits >> (i * 8)) & 0xFF
        row = (block >> 4) & 0x0F  
        col = block & 0x0F  
        result.append(hex(SBOX[7-i][row][col]))
    result.reverse()
    hex_string = ''.join(hex_value[2:] for hex_value in result)

    hex_number = int(hex_string, 16)
    return hex(hex_number)


def round_function(right_chunk , left_chunk, key):
    one_shifted = circular_shift(right_chunk,1)
    eight_shifted = circular_shift(right_chunk,8)
    two_shifted = circular_shift(right_chunk,2)
    one_shifted_and_eight_shifted = one_shifted & eight_shifted
    shifted_xor = one_shifted_and_eight_shifted ^ two_shifted
    left_chunk_xor = left_chunk ^ shifted_xor
    key_xor = key ^ left_chunk_xor
    return int(((apply_s_boxes(key_xor,SBOX=SBOX))[2:]), 16)
    

    

